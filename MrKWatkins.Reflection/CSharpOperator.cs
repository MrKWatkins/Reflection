namespace MrKWatkins.Reflection;

/// <summary>
/// The operators in C#.
/// </summary>
public enum CSharpOperator
{
    /// <summary>
    /// The addition operator <c>+</c>.
    /// </summary>
    Addition,

    /// <summary>
    /// The bitwise AND operator <c>&amp;</c>.
    /// </summary>
    BitwiseAnd,

    /// <summary>
    /// The bitwise OR operator <c>|</c>.
    /// </summary>
    BitwiseOr,

    /// <summary>
    /// The checked version of the addition operator <c>+</c>.
    /// </summary>
    CheckedAddition,

    /// <summary>
    /// The checked version of the decrement operator <c>--</c>.
    /// </summary>
    CheckedDecrement,

    /// <summary>
    /// The checked version of the division operator <c>/</c>.
    /// </summary>
    CheckedDivision,

    /// <summary>
    /// The checked version of the increment operator <c>++</c>.
    /// </summary>
    CheckedIncrement,

    /// <summary>
    /// The checked version of the multiply operator <c>*</c>.
    /// </summary>
    CheckedMultiply,

    /// <summary>
    /// The checked version of the subtraction operator <c>-</c>.
    /// </summary>
    CheckedSubtraction,

    /// <summary>
    /// The checked version of the unary negation operator <c>-</c>.
    /// </summary>
    CheckedUnaryNegation,

    /// <summary>
    /// The decrement operator <c>--</c>.
    /// </summary>
    Decrement,

    /// <summary>
    /// The division operator <c>/</c>.
    /// </summary>
    Division,

    /// <summary>
    /// The equality operator <c>==</c>.
    /// </summary>
    Equality,

    /// <summary>
    /// The XOR operator <c>^</c>.
    /// </summary>
    ExclusiveOr,

    /// <summary>
    /// The explicit conversion operator.
    /// </summary>
    Explicit,

    /// <summary>
    /// The <c>false</c> operator.
    /// </summary>
    False,

    /// <summary>
    /// The greater than operator <c>&gt;</c>.
    /// </summary>
    GreaterThan,

    /// <summary>
    /// The greater than or equal operator <c>&gt;=</c>.
    /// </summary>
    GreaterThanOrEqual,

    /// <summary>
    /// The implicit conversion operator.
    /// </summary>
    Implicit,

    /// <summary>
    /// The increment operator <c>++</c>.
    /// </summary>
    Increment,

    /// <summary>
    /// The inequality operator <c>!=</c>.
    /// </summary>
    Inequality,

    /// <summary>
    /// The left shift operator <c>&lt;&lt;</c>.
    /// </summary>
    LeftShift,

    /// <summary>
    /// The less than operator <c>&lt;</c>.
    /// </summary>
    LessThan,

    /// <summary>
    /// The less than or equal operator <c>&lt;=</c>.
    /// </summary>
    LessThanOrEqual,

    /// <summary>
    /// The NOT operator <c>!</c>.
    /// </summary>
    LogicalNot,

    /// <summary>
    /// The modulus operator <c>%</c>.
    /// </summary>
    Modulus,

    /// <summary>
    /// The multiply operator <c>*</c>.
    /// </summary>
    Multiply,

    /// <summary>
    /// The NOT operator <c>~</c>.
    /// </summary>
    OnesComplement,

    /// <summary>
    /// The right shift operator <c>&gt;&gt;</c>.
    /// </summary>
    RightShift,

    /// <summary>
    /// The subtraction operator <c>-</c>.
    /// </summary>
    Subtraction,

    /// <summary>
    /// The <c>true</c> operator.
    /// </summary>
    True,

    /// <summary>
    /// The unary negation operator <c>-</c>.
    /// </summary>
    UnaryNegation,

    /// <summary>
    /// The unary plus operator <c>+</c>.
    /// </summary>
    UnaryPlus,

    /// <summary>
    /// The unsigned right shift operator <c>&gt;&gt;&gt;</c>.
    /// </summary>
    UnsignedRightShift
}